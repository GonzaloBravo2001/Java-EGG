package Raices;

public class Raices {
    private double a;
    private double b;
    private double c;

    public Raices(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public void seta(double a) {
        this.a = a;
    }

    public void setb(double b) {
        this.b = b;
    }

    public void setc(double c) {
        this.c = c;
    }

    public double geta() {
        return a;
    }

    public double getb() {
        return b;
    }

    public double getc() {
        return c;
    }
}
